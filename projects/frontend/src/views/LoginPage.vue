<template>
  <div class="login-container">
    <!-- 左侧信息阶梯 -->
    <div class="info-panels">
      <div 
        v-for="(panel, index) in infoPanels" 
        :key="index" 
        class="info-panel"
        :style="{ 'transition-delay': index * 0.3 + 's' }"
      >
        <div class="panel-icon">{{ panel.icon }}</div>
        <div class="panel-content">
          <h3>{{ panel.title }}</h3>
          <p>{{ panel.content }}</p>
        </div>
      </div>
    </div>

    <!-- 右侧登录表单 -->
    <div class="login-form">
      <h1 class="form-title">用户登录</h1>
      <form @submit.prevent="handleLogin">
        <div class="form-group">
          <input 
            v-model="username" 
            type="text" 
            required 
            class="form-input"
            placeholder="请输入用户名"
          />
        </div>
        <div class="form-group">
          <input 
            v-model="password" 
            type="password" 
            required 
            class="form-input"
            placeholder="请输入密码"
          />
        </div>
        <button type="submit" class="submit-button">
          <span class="button-text">立即登录</span>
          <span class="button-icon">→</span>
        </button>
        <div class="form-footer">
          <router-link to="/register" class="register-link">注册新账号</router-link>
        </div>
        <p v-if="error" class="error-message">{{ error }}</p>
      </form>
    </div>
  </div>
</template>

<script>
import axios from '@/utils/axios';

export default {
  data() {
    return {
      username: '',
      password: '',
      error: '',
      infoPanels: [
        {
          icon: '🚀',
          title: '智能数据分析',
          content: '实时监控系统运行状态，提供可视化决策支持'
        },
        {
          icon: '🔒',
          title: '安全认证体系',
          content: '采用多重加密技术保障数据安全传输'
        },
        {
          icon: '📈',
          title: '趋势预测系统',
          content: '基于AI算法实现未来趋势智能预测'
        }
      ]
    };
  },
  methods: {
    async handleLogin() {
      try {
        // 修改1：确保使用完整URL路径
        const response = await axios.post(
      '/login',
      {
        username: this.username,
        password: this.password
      },
      {
        headers: {
          'Content-Type': 'application/json'
        },
        withCredentials: true
      }
    );

        // 修改2：添加响应数据验证
    if (!response.data?.token) {
      throw new Error('无效的服务器响应');
    }

    localStorage.setItem('token', response.data.token);
    localStorage.setItem('userRole', response.data.role);
    localStorage.setItem('username', response.data.username);

    this.$router.push('/home');
  } catch (err) {
    console.error('登录错误详情:', {
      error: err,
      response: err.response?.data
    });
    this.error = err.response?.data?.message || 
               err.message || 
               '登录失败，请检查凭证';
      }
    }
  },
  mounted() {
    // 触发动画
    setTimeout(() => {
      document.querySelector('.info-panels').classList.add('active');
      document.querySelector('.login-form').classList.add('active');
    }, 100);
  }
};
</script>

